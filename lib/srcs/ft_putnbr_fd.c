#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && (n = -n) && n > -2147483648)
		ft_putchar_fd('-', fd);
	if (n / 10 > 0)
		ft_putnbr_fd(n / 10, fd);
	(n == -2147483648) ? ft_putstr_fd("-2147483648", fd) :\
		ft_putchar_fd((n % 10) + 48, fd);
}
